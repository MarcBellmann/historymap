def main():
    with open(REGIONS_FILE) as f:
        regions = json.load(f)

    matched = 0
    missed = []
    for r in regions:
        if r.get("geometry") is not None:
            continue
        rid = r["id"]
        if rid in GEOMETRIES:
            r["geometry"] = GEOMETRIES[rid]
            matched += 1
        else:
            missed.append(rid)

    has_geom = sum(1 for r in regions if r.get("geometry") is not None)
    no_geom = sum(1 for r in regions if r.get("geometry") is None)

    print(f"Matched: {matched}")
    print(f"Still missing: {no_geom}")
    if missed:
        print(f"\nMissed IDs:")
        for m in missed:
            en = next((r["labels"].get("en","") for r in regions if r["id"] == m), m)
            print(f"  {m:45s} ({en})")

    with open(REGIONS_FILE, "w") as f:
        json.dump(regions, f, indent=2, ensure_ascii=False)
    print(f"\nTotal regions: {len(regions)}, with geometry: {has_geom}")


if __name__ == "__main__":
    main()
