def poly(coords):
    """Create a Polygon geometry. Auto-closes the ring."""
    if coords[0] != coords[-1]:
        coords.append(coords[0])
    return {"type": "Polygon", "coordinates": [coords]}

def mpoly(polys):
    """Create a MultiPolygon geometry."""
    rings = []
    for coords in polys:
        if coords[0] != coords[-1]:
            coords.append(coords[0])
        rings.append([coords])
    return {"type": "MultiPolygon", "coordinates": rings}

